package Jobsheet9.Tugas;

public class PakaianTugas {
    String jenis, warna, merk, ukuran;
    double harga;
    
    PakaianTugas(String jenis, String warna, String merk, String ukuran, double harga) {
        this.jenis = jenis;
        this.warna = warna;
        this.merk = merk;
        this.ukuran = ukuran;
        this.harga = harga;
        
    }
}
